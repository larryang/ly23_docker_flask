""" Operation Classes """


class Addition:
    """ wrapper for addition """

    @staticmethod
    def add(val1, val2):
        """ addition method """
        return val1 + val2


class Subtraction:
    """ wrapper for subtraction """

    @staticmethod
    def subtract(val1, val2):
        """ subtraction method """
        return val1 - val2


class Multiplication:
    """ wrapper for multiplication """

    @staticmethod
    def multiply(val1, val2):
        """ multplcation method """
        return val1 * val2
